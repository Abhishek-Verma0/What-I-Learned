/*JavaScript Assignment Operators
Assignment operators assign values to JavaScript variables.

=	    x = y	 x = y
+=  	x += y	 x = x + y
-=  	x -= y	 x = x - y
*=  	x *= y	 x = x * y
/=	    x /= y	 x = x / y
%=	    x %= y	 x = x % y
**=	    x **= y	 x = x ** y


Shift Assignment Operators

<<= 	x <<= y	    x = x << y
>>=	    x >>= y	    x = x >> y
>>>=	x >>>= y	x = x >>> y


Bitwise Assignment Operators

&=	x &= y	  x = x & y
^=	x ^= y	  x = x ^ y
|=	x |= y	  x = x | y


Logical Assignment Operators
&&=	x   &&= y	x = x && (x = y)
||=	x   ||= y	x = x || (x = y)
??=	x   ??= y	x = x ?? (x = y) */
